const mongoose = require("mongoose");

const departmentSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true
    },
    password: {
      type: String,
      required: true
    },
    role: {
      type: String,
      enum: ['DEPARTMENT', 'SUPERADMIN'],
      default: 'DEPARTMENT'
    }
  },
  { timestamps: true }
);

module.exports = mongoose.model("Department", departmentSchema);